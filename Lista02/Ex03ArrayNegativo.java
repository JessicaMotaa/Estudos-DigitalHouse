public class Ex03ArrayNegativo {
	public static void main(String[] args) {
        
        // Crie um array de inteiros e retorna a quantidade de elementos do array que são números negativos.
        
        int[] valoresNegativos = {-10,-6,-4,-2,-1};
		
		for (int i = 0; i < valoresNegativos.length; i++) {
			System.out.println("Valores: " + valoresNegativos[i]);
		}
	}
}
